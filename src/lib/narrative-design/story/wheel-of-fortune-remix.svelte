<p class="body">
	As always, there has been plenty of interesting discussion about Turn of Fortune’s Wheel
	online, particularly on Reddit. Before I came up with this remix idea, I spent a good amount
	of time reading various subreddit threads about the campaign. It is therefore quite likely
	that some of the ideas presented here were originally inspired by a random comment somewhere.
	I do like to think that I have developed this version sufficiently to call it my own, but I
	want to acknowledge that I make no strong claims of total originality.
</p>
<p class="body">
	Overall, Turn of Fortune’s Wheel is a decent campaign straight out of the book, built on
	classic RPG tropes such as amnesia and slowly uncovering what happened to you. I see it as a
	clear nod to Planescape: Torment. However, I was not entirely satisfied with how certain
	parts of the campaign were handled. Several sections felt underdeveloped or relied too heavily
	on convenient coincidences to move the story forward. The campaign also takes the players out
	of Sigil fairly early, which I found unfortunate, as Sigil is such a fascinating and unique
	setting that deserves more attention.
</p>
<p class="body">
	Lastly, there are multiple NPCs and locations where the GM is intentionally given room to
	interpret or alter events. This is partly by design and partly due to the nature of the
	campaign itself, as it can easily expand into a more sandbox-style experience.
</p>
<p class="body">
	One aspect I found disappointing was that although the campaign intends to introduce players
	to Sigil and the Outlands, it provides relatively little detailed description of many
	locations — even those that the players are expected to visit. I personally found external
	lore resources invaluable when expanding these settings. That said, this is not meant as a
	rule. Any GM should feel free to describe these locations however they see fit.
</p>

<h2 id="summary">Story Summary</h2>

<h3 id="before-starting">Before Starting</h3>
<p class="body">Generate at least the following player characters:</p>
<ul class="body-list">
	<li>Radovik Ironvein – Level 8 Hill Dwarf Warlock</li>
	<li>Fedek Vraxxis – Level 9 Dragonborn Paladin</li>
	<li>Normaris Vaelis – Level 8 Eladrin Monk</li>
	<li>Raelin Thorne – Level 18 Human Wizard</li>
</ul>
<p class="body">
	If you are playing with more than four players, create additional characters so that the
	numbers match. My version was written with four players in mind, meaning that additional
	narrative work will be required to properly integrate extra characters into the core story.
	We will explore possible solutions for that later.
</p>

<h3 id="core-premise">Core Premise</h3>
<p class="body">
	This story focuses on the events that led the players to their current predicament. As in the
	original campaign, the characters awaken in the Mortuary in Sigil with no clear knowledge of
	how they arrived there.
</p>
<p class="body">
	However, unlike in the original version where the characters must uncover alternate versions
	of themselves that have been locked away, in this remix their alternate selves are active and
	moving freely. These other halves are not passive mysteries to be discovered — they are
	agents in the world and will actively attempt to interfere with or eliminate the player
	characters.
</p>
<p class="body">
	We will use several story cues and structural elements from the original campaign. Some
	events may occur at different times or in different locations, and in some cases we may reuse
	maps from the book in entirely different contexts.
</p>
<p class="body">
	Over time, the campaign gradually reveals the truth about the characters’ former selves and
	introduces their other halves as real and ongoing threats. Toward the end of the campaign,
	the players will learn how to repair the fracture that split them. If they choose not to,
	they may retain a form of functional immortality — but this comes at a cost. The fracture in
	reality remains unstable, allowing Far Realm energies and creatures to seep through cracks in
	the foundations of existence itself.
</p>

<h3 id="background-story">Background Story</h3>
<p class="body">
	The player characters lived very different lives before the beginning of this campaign. This
	overarching background does not overwrite their personal backstories but exists alongside them
	as part of the larger cosmic narrative.
</p>
<p class="body">
	It begins nearly a thousand years ago with Raelin, who at the time was only a fledgling wizard
	with a promising future. A tragic accident changed everything. A book that sealed a powerful
	demon was accidentally opened by his youngest child, resulting in the destruction of his home
	and the loss of his family. From that point onward, Raelin dedicated his life to finding a
	way to turn back time to prevent this event.
</p>
<p class="body">
	Eventually, his search led him to Sigil and to Shemeshka the Marauder, the powerful
	arcanaloth who runs the Fortune’s Wheel casino. Shemeshka did not ignore the opportunity
	presented to her. Instead, she struck a bargain with Raelin: she would extend his life, and in
	return he would assist her in fulfilling her own long-term obligations, particularly those
	involving powerful devils invested in the eternal Blood War.
</p>
<p class="body">
	Together, they devised a plan centered on the Great Modron March. Raelin understood that this
	plan would take centuries to prepare. To accomplish it, he founded a secretive organization
	known as the Circle of Coriolis.
</p>
<p class="body">
	Officially, the Circle exists to gather knowledge for knowledge’s sake. In reality, this is
	largely a façade. Raelin’s true aim is to study the Modron March in depth and eventually
	harness its power. Over time, he gathers powerful allies — the other player characters — and
	expands the Circle’s influence across Sigil, the Outlands, and beyond.
</p>
<p class="body">
	The other members are aware that the Modron March is central to the organization’s long-term
	plan. However, they believe its purpose is to provide Shemeshka with a powerful cosmic tool,
	one that will strengthen her position and serve as repayment for their extended lifespans.
	None of them know that Raelin ultimately intends to use it to reverse time.
</p>

<h3 id="the-schism">The Schism</h3>
<p class="body">
	At some point, tensions arise within the Circle. Normaris breaks away from the organization.
	Radovik, believing her to be a liability, arranges an assassination attempt. The attempt
	fails and instead results in the deaths of individuals close to Normaris.
</p>
<p class="body">
	In response, Normaris begins systematically targeting the Circle’s operations in Sigil,
	dismantling bases and searching for those responsible.
</p>
<p class="body">
	As the conflict escalates, the remaining members of the Circle decide to act before the
	situation spirals further out of control. They move forward with an accelerated attempt to
	take control of the Modron March. At a crucial moment, Normaris intervenes and destroys an
	important magical focusing device intended to harness and channel the energy emanating from
	the Modrons.
</p>
<p class="body">
	The consequences are catastrophic. Instead of controlled manipulation, the unleashed energy
	fractures reality itself. Each member present is split into two distinct versions: their
	original self — albeit psychologically altered — and the versions that become the player
	characters.
</p>

<h3 id="aftermath">Aftermath</h3>
<p class="body">
	The original selves recover more quickly, largely thanks to Raelin’s intervention. Radovik
	quickly arranges assistance to dispose of the fractured bodies, intending for them to be
	dumped into a deeper drainage shaft in Sigil’s sewers.
</p>
<p class="body">
	The plan fails. Sewer-dwelling creatures scare off the hired help, and in the chaos Normaris
	also manages to escape.
</p>
<p class="body">
	Shortly thereafter, Sigil’s sewer maintenance workers discover the bodies. Believing them
	dead, they contact the Mortuary to collect them.
</p>
<p class="body">And that is where the campaign begins.</p>

