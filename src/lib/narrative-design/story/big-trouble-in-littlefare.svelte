		<p class="body">
			This scenario isn’t particularly original. I can’t locate the original
			Reddit post anymore, but the core idea was a one-shot in which a dragon is
			terrorising a small town, pushing the party into a series of morally
			ambiguous (“shades of grey”) decisions.
		</p>
		<p class="body">
			In this version, the adventure is designed for low-level characters and
			first-time players. Instead of a real dragon, the threat is an illusory
			dragon, allowing the scenario to create tension and spectacle without
			overwhelming inexperienced players with an unwinnable fight.
		</p>
		<p class="body">
			The one-shot is structured to showcase the core elements of D&amp;D:
		</p>
		<ul class="body-list">
			<li>Problem-solving with multiple valid approaches</li>
			<li>
				Social interaction where suspicion does not automatically equal
				villainy
			</li>
			<li>Optional, clearly contained combat encounters</li>
		</ul>
		<p class="body">
			Combat can occur at a few points, but none of it is mandatory. Depending
			on pacing, table energy, or time constraints, the GM can easily skip or
			downplay these encounters without breaking the narrative.
		</p>
		<p class="body">
			Overall, the scenario is intentionally flexible. It gives the GM room to
			either slow things down for exploration and roleplay or push the story
			forward when needed. In terms of difficulty, it sits at a low to medium
			level, primarily due to narrative decision-making rather than mechanical
			challenge.
		</p>

		<h2 id="summary">Story Summary</h2>
		<p class="body">
			The town of Littlefare has endured a difficult year. For the past twelve
			months, it has been terrorised by what the townsfolk believe to be a
			vicious and greedy red dragon. Every two to three weeks, the creature
			demands tribute. Whenever the town fails to meet its demands, a house is
			burned or destroyed as punishment.
		</p>
		<p class="body">
			At first, Littlefare manages to comply, but its resources steadily
			dwindle. As the situation worsens, “dragon protection” becomes a source
			of revenue. Caravans passing through the town are forced to surrender a
			fifth of their cargo or face threats of an “unsafe journey ahead,” as they
			would be left unprotected from the dragon’s wrath.
		</p>
		<p class="body">
			In truth, the dragon does not exist.
		</p>
		<p class="body">
			Behind the scenes, a malevolent barghest named Mogawg has formed a
			mutually beneficial alliance with a cunning doppelganger, Naa Mio, and a
			small band of orcs. The barghest feeds on fear, and the slow erosion of
			Littlefare’s morale has left it well-fed and complacent.
		</p>
		<p class="body">
			To maintain the deception, Naa Mio first murdered the town’s well-liked
			mayor, Aelric Venn, after closely observing him and learning his basic
			mannerisms. Disguised as the mayor, Naa Mio now rules Littlefare from the
			shadows.
		</p>
		<p class="body">
			Using simple disguise tools and minor magic, Naa Mio has also outfitted
			several orcs to appear as the mayor’s personal city guards. Together, they
			stage the illusion of a dragon threat: Mogawg uses its shapechanging and
			levitation abilities to create convincing “dragon sightings,” while the
			orcs periodically burn outlying homes and kill their occupants to
			reinforce the illusion.
		</p>
		<p class="body">
			Once fear has fully taken hold, Naa Mio escalates control. Hired ruffians
			are installed as additional “tax collectors,” ensuring that no caravan
			passes through Littlefare without paying the toll. Those who refuse are
			marked for raids carried out at a safe distance by Mogawg and the orcs.
		</p>
		<p class="body">
			Several prominent figures in Littlefare suspect that something is wrong,
			but fear keeps them silent. They worry for their families, their friends,
			and their own lives.
		</p>
		<div class="dm-block">
			<p class="dm-title">The Truth of Littlefare (GM Only)</p>
			<p class="body">There is no dragon.</p>
			<p class="body">
				The town is controlled by Naa Mio, a doppelganger who murdered the
				real mayor, Aelric Venn, and assumed his identity. Power is maintained
				through fear, staged destruction, and a pact with Mogawg, a barghest
				that feeds on terror and flesh.
			</p>
			<p class="body">
				Orcs loyal to Naa Mio enforce tribute and tolls, while Mogawg sustains
				the illusion of a dragon through shapechanging displays and selective
				slaughter.
			</p>
			<p class="body">The town survives through compliance.</p>
		</div>

		<h2 id="running">Running This Adventure</h2>
		<p class="body">
			The players are Harper Investigators first, merchant envoys second. The
			emphasis should be on doing discreet investigation rather than punching
			every NPC in the face.
		</p>
		<p class="body">
			Having said that, this one-shot is quite light on combat events, partly
			because of their time-consuming nature. There are plenty of options for
			adding them, such as an extra encounter with ruffians on the streets of
			the town.
		</p>
		<div class="dm-block">
			<p class="dm-title">One-Shot Design Principle: Convergence</p>
			<p class="body">
				No matter how the players approach the investigation—combat, stealth,
				or social play—the adventure is designed to converge on a final
				confrontation with the source of the fear. However, the dining hall
				encounter is also an optional spot to cut things short if the session
				needs to be wrapped up and the conclusion narrated from there onward.
			</p>
			<p class="body">
				If pacing slows or players stall, introduce information that points
				them forward.
			</p>
		</div>

		<h4 id="act-1" class="act-title">ACT I – The Road to Littlefare</h4>
		<h5 class="act-subtitle">Overview</h5>
		<p class="body">
			For several months, trade caravans traveling through the Littlefare
			region have failed to arrive in Waterdeep or have returned partially
			stripped of their cargo. In response, a loose alliance of merchants has
			pooled resources to send a small delegation to investigate the
			disruption.
		</p>
		<p class="body">
			The party is officially hired as part of this merchant effort.
		</p>
		<p class="body">
			Unbeknownst to the merchants, the Harpers are also watching the situation
			closely. While rumors of a dragon extorting a small town are widespread,
			no reliable proof exists. The Harpers suspect something more subtle and
			assign the party a secondary objective: investigate Littlefare discreetly
			and determine whether the dragon threat is real.
		</p>
		<p class="body">
			The party is instructed to maintain their cover as merchant
			representatives.
		</p>
		<h5 class="act-subtitle">Party Setup</h5>
		<p class="body">The characters already know each other.</p>
		<p class="body">
			They may be colleagues, associates, or trusted contacts who have worked
			together before, but they have all been deliberately selected for this
			mission. They are aware that:
		</p>
		<ul class="body-list">
			<li>Caravans have gone missing or been extorted</li>
			<li>Littlefare is the common link</li>
			<li>Claims of a dragon have circulated, but evidence is thin</li>
		</ul>
		<p class="body">
			The adventure begins with the party traveling together along the road
			toward Littlefare.
		</p>
		<h5 class="act-subtitle">The Road Encounter</h5>
		<p class="body">
			A short distance from the town, the party encounters a stopped caravan on
			the road.
		</p>
		<p class="body">
			A small group of ruffians has blocked passage and is demanding a toll
			equal to one-fifth of the caravan’s cargo, claiming it is required for
			“dragon protection.” The caravan driver refuses to pay and looks to the
			party—clearly well-equipped—for help.
		</p>
		<p class="body">
			There are four ruffians in total. They are confident but not disciplined,
			and they do not appear eager for a prolonged fight.
		</p>
		<p class="body">This encounter establishes:</p>
		<ul class="body-list">
			<li>The toll system</li>
			<li>The use of the dragon threat as justification</li>
			<li>Early player agency and consequence</li>
		</ul>
		<h5 class="act-subtitle">Resolving the Encounter</h5>
		<p class="body">The situation can resolve in several ways:</p>
		<ol class="body-list">
			<li>
				<strong>Social Resolution.</strong> The party may attempt to persuade
				the ruffians to stand down, intimidate them into backing off, or
				convince them to escort the caravan and party to Littlefare to “sort it
				out there.” If successful, one ruffian is sent ahead to warn the mayor
				of the party’s arrival.
			</li>
			<li>
				<strong>Compliance.</strong> The party may convince the caravan driver
				to pay the toll and continue on their way. If this happens, the
				ruffians again send word ahead to the mayor.
			</li>
			<li>
				<strong>Combat.</strong> If the encounter turns violent: once two
				ruffians are killed or incapacitated, one remaining ruffian attempts to
				flee toward Littlefare to warn the mayor. This escape attempt occurs
				unless the party actively prevents it. If all ruffians are dealt with
				before anyone escapes, no warning is sent.
			</li>
		</ol>
		<div class="dm-block">
			<p class="dm-title">Mayor Awareness Trigger</p>
			<p class="body">
				If ruffians survive, are persuaded, escort the caravan, or collect
				tribute, word reaches the mayor.
			</p>
			<p class="body">
				If all ruffians are eliminated quietly before anyone escapes, the mayor
				remains unaware of the party’s approach.
			</p>
		</div>
		<h5 class="act-subtitle">End of Act I</h5>
		<p class="body">
			Regardless of outcome, the party continues onward toward Littlefare.
			 Act I ends as the town of Littlefare comes into view.
		</p>

		<h4 id="act-2" class="act-title">ACT II – Arrival, Control, and the Unraveling of Fear</h4>
		<h5 class="act-subtitle">Overview</h5>
		<p class="body">
			Act II is the investigative heart of the adventure. The party arrives in
			Littlefare and begins to uncover cracks in the town’s official story.
			Fear, compliance, and quiet complicity define daily life here. Everyone
			knows something is wrong—few are willing to say it aloud.
		</p>
		<p class="body">
			This act is intentionally flexible. The party may move cautiously or
			aggressively, socially or stealthily. Regardless of approach, evidence
			accumulates and pressure increases, leading inevitably to Act III.
		</p>
		<h5 class="act-subtitle">Arrival in Littlefare</h5>
		<p class="body">
			As the party enters Littlefare, several things are immediately clear:
		</p>
		<ul class="body-list">
			<li>The town is visibly run down, even by rural standards.</li>
			<li>Some buildings show signs of fire damage or hasty repairs.</li>
			<li>Streets are quiet, and people avoid lingering outdoors.</li>
			<li>Townsfolk avert their gaze, close shutters, and retreat inside when strangers pass.</li>
		</ul>
		<div class="dm-block">
			<p class="dm-title">Tone of Littlefare</p>
			<p class="body">Littlefare is not panicked.</p>
			<p class="body">It is obedient.</p>
			<p class="body">
				People move quickly, speak quietly, and avoid attention. The town
				survives by not being noticed.
			</p>
		</div>
		<h5 class="act-subtitle">Arrival Branch: Is the Mayor Aware?</h5>
		<p class="body">How the party is received depends on events in Act I.</p>
		<h5 class="act-subtitle">Branch A – Mayor Aware of the Party’s Arrival</h5>
		<p class="body">
			If word of the party reached Littlefare ahead of them, they receive a
			formal reception shortly after entering town.
		</p>
		<p class="body">
			At the town center, the mayor greets the party alongside:
		</p>
		<ul class="body-list">
			<li>A small number of personal guards</li>
			<li>Several notable figures from the town</li>
		</ul>
		<p class="body">
			The mayor presents himself as polite, cooperative, and relieved by
			outside help. He claims that he has repeatedly requested assistance
			regarding the dragon threat (this is a lie) and expresses willingness to
			support the party’s investigation.
		</p>
		<p class="body">
			As a public gesture of goodwill, he offers the party lodging at the
			town’s only inn and instructs Martha Grayblum, the innkeeper, to host them
			at the town’s expense. Martha agrees, but hesitantly.
		</p>
		<p class="body">
			The meeting is calm, orderly, and clearly staged.
		</p>
		<div class="dm-block">
			<p class="dm-title">Running the Mayor (Reminder)</p>
			<p class="body">The mayor is calm, controlled, and accommodating.</p>
			<p class="body">He does not argue.</p>
			<p class="body">He redirects.</p>
			<p class="body">
				If questioned directly, he answers around the truth.
			</p>
		</div>
		<h5 class="act-subtitle">Branch B – Mayor Unaware of the Party’s Arrival</h5>
		<p class="body">
			If the mayor has not been warned, the party enters Littlefare without
			fanfare.
		</p>
		<p class="body">
			There is no escort or official greeting. The party is free to explore,
			speak to townsfolk, and form impressions organically. NPCs remain cautious,
			and no one volunteers information freely.
		</p>
		<p class="body">
			As the party becomes more inquisitive, guards may eventually request a
			meeting with the mayor, or the party may seek him out themselves.
		</p>
		<div class="dm-block">
			<p class="dm-title">GM Guidance: Let Them Breathe</p>
			<p class="body">
				If the mayor is unaware, allow the party time to investigate freely.
				This freedom should feel fragile.
			</p>
			<p class="body">
				Once attention is drawn, oversight follows quickly.
			</p>
		</div>
		<h5 class="act-subtitle">The First Night</h5>
		<p class="body">
			The party arrives late in the day.
		</p>
		<ul class="body-list">
			<li>Shops are closing</li>
			<li>Streets are quiet</li>
			<li>The town is settling in for the night</li>
		</ul>
		<p class="body">
			This naturally limits investigation on the first evening and pushes the
			party toward rest.
		</p>
		<h5 class="act-subtitle">The Morning Dew Inn</h5>
		<p class="body">
			Littlefare has only one inn: The Morning Dew, run by Martha Grayblum.
		</p>
		<p class="body">
			The inn is modest, worn, but serviceable. Martha is polite, guarded, and
			clearly stressed. She watches newcomers carefully and chooses her words
			with care.
		</p>
		<p class="body">
			The party almost certainly stays here on their first night.
		</p>
		<div class="npc-table">
			<div class="npc-row">
				<div class="npc-head">
					<div>
						<strong>Martha Grayblum</strong>
						<span class="npc-meta">Species: Human</span>
					</div>
					<span class="npc-role">Innkeeper</span>
				</div>
				<p class="npc-notes">
					Martha is afraid, though she tries to hide it. She fears for her
					family, her livelihood, and her safety. She genuinely believes the
					dragon threat is real. She believes the mayor is doing the best he can
					under impossible circumstances.
				</p>
				<p class="npc-notes">In conversation:</p>
				<ul class="npc-list">
					<li>She avoids speculation.</li>
					<li>She deflects accusations.</li>
					<li>She discourages risky behaviour “for everyone’s sake.”</li>
				</ul>
				<p class="npc-notes">If asked about the mayor, she shares the public story:</p>
				<p class="npc-notes">
					Seventeen years ago, during a catastrophic flood, the mayor lost his
					wife and daughter. During that crisis, he took decisive leadership and
					coordinated relief efforts that saved much of the town from worse
					destruction. Because of this, he is still widely respected.
				</p>
				<p class="npc-notes">This story is true—but incomplete.</p>
				<p class="npc-notes">
					<strong>(If Mayor is unaware)</strong> Martha does not want trouble.
					If the party presses too hard, she will quietly alert the mayor. If
					they remain polite and vague, she keeps quiet—for now.
				</p>
			</div>
		</div>
		<h5 class="act-subtitle">Day Two – The Investigation Proper</h5>
		<p class="body">
			Day Two marks the true opening of Act II.
		</p>
		<p class="body">
			The town is active again, but conversations remain short and cautious.
			This is when the party is expected to investigate locations, question key
			NPCs, and begin uncovering contradictions.
		</p>
		<p class="body">Leads may come from:</p>
		<ul class="body-list">
			<li>The mayor (if he is managing the party)</li>
			<li>Harper background information</li>
			<li>NPC conversations</li>
		</ul>
		<div class="dm-block">
			<p class="dm-title">Investigation Pacing</p>
			<p class="body">
				Act II works best if the party speaks to 2–3 NPCs before moving on.
			</p>
			<p class="body">Do not require every lead to be followed.</p>
		</div>
		<h5 class="act-subtitle">Locations of Interest in Littlefare</h5>
		<p class="body">
			During Day Two, the party is expected to investigate several key locations
			within and around Littlefare. Each location ties directly to one or more
			NPCs and provides partial insight into the true situation. No single
			location reveals the full truth on its own.
		</p>
		<p class="body">The locations may be explored in any order.</p>
		<h5 class="act-subtitle">The Shrine of Chauntea</h5>
		<p class="body">
			The primary religious site in Littlefare is a modest but well-kept shrine
			dedicated to Chauntea, overseen by Berrik Evermoor.
		</p>
		<ul class="body-list">
			<li>The shrine is quiet, functional, and heavily used.</li>
			<li>Many townsfolk come here seeking reassurance rather than guidance.</li>
			<li>Offerings are modest but frequent.</li>
			<li>Berrik Evermoor can usually be found here during the day.</li>
		</ul>
		<p class="body">
			He presents compliance as a moral necessity and urges the party to avoid
			confrontation. While sincere, he is clearly exhausted and burdened by
			fear. With gentle pressure, he admits that:
		</p>
		<ul class="body-list">
			<li>Tribute demands are increasing</li>
			<li>The mayor has become less approachable</li>
		</ul>
		<p class="body">
			He refuses to speculate further, fearing what open resistance might bring.
		</p>
		<div class="npc-table">
			<div class="npc-row">
				<div class="npc-head">
