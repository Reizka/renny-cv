		</div>
		<h5 class="act-subtitle">The Tradepost</h5>
		<p class="body">
			The tradepost sits near the main road and serves as the administrative
			heart of caravan traffic through Littlefare.
		</p>
		<ul class="body-list">
			<li>Caravans stop here to register goods and pay fees.</li>
			<li>The building is functional, orderly, and closely monitored.</li>
			<li>Havaldan, the tradepost merchant, oversees operations.</li>
		</ul>
		<p class="body">He explains that:</p>
		<ul class="body-list">
			<li>Tribute is collected here every two weeks</li>
			<li>It is delivered to a clearing outside town</li>
			<li>The mayor’s private retinue handles all deliveries</li>
		</ul>
		<p class="body">
			Havaldan will cooperate cautiously, providing only records that do not
			implicate him directly.
		</p>
		<div class="npc-table">
			<div class="npc-row">
				<div class="npc-head">
					<div>
						<strong>Havaldan Thorpe</strong>
						<span class="npc-meta">Species: Gnome</span>
					</div>
					<span class="npc-role">Trading post merchant</span>
				</div>
				<p class="npc-notes">
					Havaldan oversees the town’s trading post and caravan traffic.
				</p>
				<p class="npc-notes">
					He understands more than most and is materially rewarded for his
					cooperation. While he has private doubts, his primary concern is
					self-preservation.
				</p>
				<p class="npc-notes">If questioned carefully, he admits that:</p>
				<ul class="npc-list">
					<li>Tribute is paid every two weeks</li>
					<li>Collected at the trading post</li>
					<li>Transported to a clearing roughly one hour’s walk from town</li>
					<li>Always delivered by the mayor’s private retinue, never by him</li>
				</ul>
				<p class="npc-notes">
					He presents this as standard procedure and avoids speculation.
				</p>
			</div>
		</div>
		<h5 class="act-subtitle">Investigating the Trading Records</h5>
		<p class="body">
			Havaldan keeps orderly books and will provide only safe records if asked
			directly.
		</p>
		<p class="body">
			If the party attempts to access additional records:
		</p>
		<ul class="body-list">
			<li>Stealth DC 16 without distraction</li>
			<li>Easier with distraction (GM discretion)</li>
		</ul>
		<p class="body">
			With an Investigation check (DC 12), the party finds a separate booklet
			tracking caravan movements.
		</p>
		<p class="body">Several caravans are marked with a strange symbol.</p>
		<p class="body">
			If the caravan encountered in Act I paid tribute or returned to town, it
			bears no mark. If it passed without paying, it bears the symbol.
		</p>
		<p class="body">
			A History check (DC 16) allows recognition of the symbol from missing
			caravan records in Waterdeep. Lower the DC for characters with merchant
			backgrounds.
		</p>
		<div class="dm-block">
			<p class="dm-title">The Caravan Ledger</p>
			<p class="body">Caravans marked with the symbol are later pillaged.</p>
			<p class="body">The toll system is punitive, not protective.</p>
		</div>
		<h5 class="act-subtitle">Elric’s Hovel</h5>
		<p class="body">
			Elric Stoneford, the former guard captain, lives in a small, poorly
			maintained hovel on the edge of town.
		</p>
		<ul class="body-list">
			<li>The building shows neglect rather than poverty.</li>
			<li>Empty bottles and signs of disuse are obvious.</li>
		</ul>
		<p class="body">
			Elric is often intoxicated, bitter, and isolated, but he is not
			incoherent.
		</p>
		<p class="body">He openly questions the dragon narrative, noting that:</p>
		<ul class="body-list">
			<li>Burned houses do not resemble dragon attacks</li>
			<li>No bodies are ever found</li>
			<li>The mayor is not the man he once was</li>
		</ul>
		<p class="body">
			He has no proof, but his instincts are correct. He is less afraid than
			others—and more alone.
		</p>
		<div class="npc-table">
			<div class="npc-row">
				<div class="npc-head">
					<div>
						<strong>Elric Stoneford</strong>
						<span class="npc-meta">Species: Half Orc</span>
					</div>
					<span class="npc-role">Former guard captain</span>
				</div>
				<p class="npc-notes">
					Elric Stoneford is still technically the guard captain but has been
					sidelined for most of the past year.
				</p>
				<ul class="npc-list">
					<li>He has developed a drinking problem.</li>
					<li>He feels isolated and disrespected.</li>
					<li>He openly advocates fighting the dragon.</li>
				</ul>
				<p class="npc-notes">Elric points out that:</p>
				<ul class="npc-list">
					<li>Burned houses do not resemble dragon attacks.</li>
					<li>No bodies are ever left behind.</li>
					<li>The mayor is not the man he once was.</li>
				</ul>
				<p class="npc-notes">
					He has no proof—but his instincts are correct.
				</p>
				<p class="npc-notes">
					<strong>Elric’s Value:</strong> Elric provides emotional truth rather
					than evidence. He confirms that doubt exists—but has been silenced.
				</p>
			</div>
		</div>
		<h5 class="act-subtitle">The Mayor’s Keep</h5>
		<p class="body">
			The mayor’s residence sits at the center of Littlefare. It is not a
			castle, but a well-built stone house in a Central European style—solid,
			respectable, and clearly above the surrounding structures.
		</p>
		<ul class="body-list">
			<li>Guards are always present.</li>
			<li>Access is controlled but not overtly hostile.</li>
			<li>The building projects authority without excess.</li>
		</ul>
		<p class="body">Depending on mayor awareness:</p>
		<ul class="body-list">
			<li>The party may be welcomed openly</li>
			<li>Or may need to request an audience</li>
		</ul>
		<p class="body">
			The keep becomes the focal point of Act III, but limited interaction may
			occur here during Act II if the mayor is managing the party.
		</p>
		<div class="dm-block">
			<p class="dm-title">The Mayor’s Presentation</p>
			<p class="body">The keep is meant to reassure, not intimidate.</p>
			<p class="body">Everything about it says: order is being maintained.</p>
		</div>
		<h5 class="act-subtitle">The Tribute Clearing</h5>
		<p class="body">
			The tribute delivery site lies roughly one hour’s walk outside town,
			beyond the worked fields.
		</p>
		<ul class="body-list">
			<li>The surrounding land is flat farmland.</li>
			<li>A river runs nearby—the site of the historic flood.</li>
		</ul>
		<p class="body">At the clearing, the party finds:</p>
		<ul class="body-list">
			<li>An upturned cart not caused by great force</li>
			<li>A missing wheel</li>
			<li>Routine cart tracks in and out</li>
			<li>A single claw mark too small for a dragon</li>
			<li>No scorched ground, no massive tracks, no remains</li>
		</ul>
		<h5 class="act-subtitle">Nature and Tracking Clues</h5>
		<ul class="body-list">
			<li>
				Perception DC 12 (Druid/Ranger): Spot an animal (bird, squirrel) that can
				be spoken to if player has speak with animals or a similar ability.
			</li>
			<li>
				The animal describes a strange creature that sometimes has wings,
				sometimes not
			</li>
			<li>Mentions “people who aren’t people”</li>
		</ul>
		<p class="body">Survival or Nature DC 15:</p>
		<ul class="body-list">
			<li>Identifies non-dragon tracks (barghest)</li>
			<li>
				Tracks lead into fields and disappear where the land was recently
				plowed
			</li>
		</ul>
		<p class="body">This confirms:</p>
		<ul class="body-list">
			<li>A monster is involved</li>
			<li>The “dragon” story does not fit the evidence</li>
		</ul>
		<div class="dm-block">
			<p class="dm-title">Absence as Evidence</p>
			<p class="body">There are no dragon signs here.</p>
			<p class="body">That is the point.</p>
		</div>
		<h5 class="act-subtitle">Using the Locations</h5>
		<p class="body">The party does not need to visit every location.</p>
		<p class="body">Each location:</p>
		<ul class="body-list">
			<li>Confirms part of the lie</li>
			<li>Reinforces the atmosphere of fear</li>
			<li>Points indirectly toward the mayor</li>
		</ul>
		<p class="body">
			Once the party has visited two or three locations, Act II should naturally
			transition toward confrontation and Act III.
		</p>
		<h5 class="act-subtitle">End of Act II</h5>
		<p class="body">By the end of Act II:</p>
		<ul class="body-list">
			<li>The dragon story no longer holds up</li>
			<li>The mayor’s behavior is suspect</li>
			<li>Fear is clearly being enforced, not endured</li>
		</ul>
		<p class="body">
			If the mayor is aware of the party’s actions, they receive an invitation
			to dine that evening. If not, this will require more narrative decision making from the GM. 
			The players should have a sense that the mayor is hiding something. 
			Meaning that if the players are not joining for dinner they should investigate the keep nontheless.  
		</p>
		<div class="dm-block">
			<p class="dm-title">Sneaking to the Keep</p>
			<p class="body">Note that ACT III assumes the party is joining the mayor for dinner. 
				GMs should be prepared to handle the case where the party does not join the mayor for dinner, and instead sneaks into the keep.
				You can flavour this type of approach with party witnessing a conversation that reveals the mayor's true intentions 
				or the presence of disguised guards or even the Bargheast's location.</p>
		</div>
		<p class="body">
			Act II ends as the truth begins to surface and Act III becomes unavoidable.
		</p>
		<h4 id="act-3" class="act-title">ACT III – Masks Off</h4>
		<h5 class="act-subtitle">Overview</h5>
		<p class="body">
			Act III is the collapse of the lie. Whether through violence, deception,
			or exposure, the truth about Littlefare can no longer be contained. This
			act is intentionally volatile, but all paths converge toward a final
			reckoning with the true source of fear.
		</p>
		<div class="dm-block">
			<p class="dm-title">Act III Design Goal</p>
			<p class="body">
				Act III ideally leads the party toward the barghest’s lair and final confrontation.
			</p>
			<p class="body">
				If pacing slows or outcomes branch too far, introduce information that
				points them forward.
			</p>
		</div>
		<h5 class="act-subtitle">The Invitation</h5>
		<p class="body">
			Act III begins once the party enters the mayor’s residence.
			The invitation is a clear signal that the mayor  is attempting to manage the party’s investigation and control the narrative.
		</p>
		<ul class="body-list">
			<li>A polite summons</li>
			<li>A guarded request</li>
			<li>Guards escorting the party “for a conversation”</li>
		</ul>
		<h5 class="act-subtitle">The Mayor’s Residence</h5>
		<p class="body">
			The mayor’s house is solid and defensible, but not extravagant.
		</p>
		<p class="body"> 4 - 6 members of the mayor’s personal guard are present.</p>
		<p class="body">
			These guards are actually orcs in disguise, concealed with mundane makeup
			and minor magic.
		</p>
		<h5 class="act-subtitle">Spotting the Disguises</h5>
		<ul class="body-list">
			<li>Passive Perception: No effect</li>
			<li>Active Perception DC 16:</li>
			<li>Something about the guards is off</li>
			<li>Their appearance is unnaturally consistent</li>
			<li>
				It is clear disguises are involved, but not immediately that they are
				orcs
			</li>
		</ul>
		<p class="body">The atmosphere is polite, controlled, and tense.</p>
		<div class="dm-block">
			<p class="dm-title">Disguised Guards</p>
			<p class="body">
				The guards are meant to feel wrong, not immediately exposed.
			</p>
			<p class="body">Let suspicion build before revelation.</p>
		</div>
		<h5 class="act-subtitle">The Dinner</h5>
		<p class="body">
			The mayor welcomes the party warmly and behaves as he has throughout the
			adventure:
		</p>
		<ul class="body-list">
			<li>Calm</li>
			<li>Cooperative</li>
			<li>Helpful</li>
		</ul>
		<p class="body">
			He asks how the investigation is progressing and gently probes for what
			the party knows.
		</p>
		<p class="body">The party is led to a dining hall.</p>

			<div class="dm-block">
			<p class="dm-title">Dont forget that Mayor Is a Doppelganger</p>
			<p class="body">
				He can read surface-level thoughts: intent, suspicion, emotional focus.
			</p>
			<p class="body">
				Meaning that will likely know if the players are pretending to eat the food or otherwise cause hijinks.
				He can also use this to subtly manipulate the conversation.
			</p>

			<p class="body">
				If part of the party leaves the dinner under pretense, the mayor will know and may react accordingly.
				He may allow it, or he may use it as an opportunity to isolate and capture the character/s.
			</p>
			
		</div>

		<h5 class="act-subtitle">The Meal</h5>
		<ul class="body-list">
			<li>Well-prepared but restrained</li>
			<li>Pleasant, not lavish</li>
			<li>Appropriate to a struggling town</li>
		</ul>
		<p class="body">The food and drink are drugged.</p>
		<div class="dm-block">
			<p class="dm-title">Handling the Drugging</p>
			<p class="body">Do not track individual bites.</p>
			<p class="body">Let conversation play out.</p>
			<p class="body">
				Once most characters have reasonably partaken, trigger the effect.
			</p>
			<p class="body"><strong>Constitution Save DC 12</strong></p>
			<p class="body">Success: Drowsy but functional</p>
			<p class="body">Failure: Sluggish or unconscious (GM pacing)</p>
		</div>
	
		<h5 class="act-subtitle">Player Agency During the Dinner</h5>
		<p class="body">Players may attempt to:</p>
		<ul class="body-list">
			<li>Slip away under pretense</li>
			<li>Investigate the residence</li>
			<li>Observe guards more closely</li>
		</ul>
		<p class="body">Guards escort lone characters.</p>
		<p class="body">
			Stealth DC 14 allows slipping away in most situations (GM discretion).
		</p>
		<h5 class="act-subtitle">Investigating the Upper Floors</h5>
		<p class="body">If players reach the mayor’s private quarters:</p>
		<ul class="body-list">
			<li>Most documents are mundane</li>
			<li>
				Investigation DC 16 reveals a hidden compartment containing parchments
				detailing:
			</li>
		</ul>
		<ul class="body-list">
			<li>The planned takeover of Littlefare</li>
			<li>Tribute logistics</li>
			<li>References to Mogawg, a barghest</li>
			<li>Notes describing the arrangement as a contract</li>
		</ul>
		<p class="body">
			The documents are signed under the mayor’s true name: Naa Mio.
		</p>
		<p class="body">This is undeniable proof.</p>
		<div class="dm-block">
			<p class="dm-title">What the Documents Prove</p>
			<p class="body">The dragon is a fabrication.</p>
			<p class="body">The mayor is not who he claims to be.</p>
			<p class="body">The barghest is the true enforcer.</p>
			<p class="body">At this point, the conspiracy is exposed.</p>
		</div>
		<h5 class="act-subtitle">Escalation Paths</h5>
		<p class="body">Act III can unfold in several ways. All are valid.</p>
		<h5 class="act-subtitle">Path A – Open Combat at the Keep</h5>
		<p class="body">If the party confronts or attacks:</p>
		<ul class="body-list">
			<li>Combat breaks out in the dining hall</li>
			<li>Present: Naa Mio (Doppelganger) and four disguised orcs</li>
		</ul>
		<p class="body"><strong>Combat Notes</strong></p>
		<ul class="body-list">
			<li>Naa Mio prefers the party alive for interrogation</li>
			<li>If Naa Mio lives, orcs attempt nonlethal takedowns</li>
			<li>If Naa Mio dies, orcs fight to kill</li>
		</ul>
		<p class="body">
			This is a dangerous but fair encounter for a level 4 party.
		</p>
		<h5 class="act-subtitle">Path B – Partial Capture</h5>
		<p class="body">
			If some characters succumb to the drug and others resist:
		</p>
		<ul class="body-list">
			<li>Chaos erupts</li>
			<li>Captured characters are transported directly to the barghest</li>
			<li>Remaining characters may hide or pursue</li>
		</ul>
		<p class="body">This path strongly encourages pursuit.</p>
		<h5 class="act-subtitle">Path C – Full Capture (The Jail)</h5>
		<p class="body">If all characters are captured:</p>
		<ul class="body-list">
			<li>They awaken in a stone cell beneath the keep</li>
		</ul>
		<p class="body"><strong>Escape Options</strong></p>
		<ul class="body-list">
			<li>Persuasion DC 12: Convince the human guard using evidence</li>
			<li>Perception DC 14: Discover a rusted floor grate hidden under moldy blankets</li>
		</ul>
		<p class="body"><strong>Fail-Forward:</strong></p>
		<ul class="body-list">
			<li>Elric intervenes</li>
			<li>Otherwise Berrik</li>
			<li>Otherwise Martha</li>
			<li>Otherwise a frightened citizen</li>
		</ul>
		<p class="body">
			Once free, the party learns that prisoners or supplies are being moved
			outside town.
		</p>
		<div class="dm-block">
			<p class="dm-title">Fail-Forward Rule</p>
			<p class="body">Act III should never stall.</p>
			<p class="body">If players fail every check, someone helps them.</p>
		</div>
		<h5 class="act-subtitle">Convergence: Finding the Lair</h5>
		<p class="body">
			Regardless of the path taken, the party learns the location of Mogawg’s
			lair.
		</p>
		<p class="body">This may come from:</p>
		<ul class="body-list">
			<li>Notes found on Naa Mio or an orc</li>
			<li>Following a prisoner cart</li>
			<li>Information gained after escaping captivity</li>
		</ul>
		<p class="body">
			The lair lies beyond the tribute clearing, at the edge of the farmland.
		</p>
		<h5 class="act-subtitle">The Barghest’s Lair</h5>
		<p class="body"><strong>Location</strong></p>
		<ul class="body-list">
			<li>A shallow cave just beyond the worked fields</li>
			<li>Close enough for supply runs</li>
			<li>Unpleasant even for the orcs</li>
		</ul>
		<p class="body">
			Orcs leave quickly, muttering about the creature below.
		</p>
		<h5 class="act-subtitle">Inside the Cave</h5>
		<ul class="body-list">
			<li>Piles of valuables (ignored by Mogawg)</li>
			<li>Slaughtered livestock</li>
			<li>Human bones</li>
			<li>Signs of recent feeding</li>
		</ul>
		<p class="body">The barghest feeds on fear—and flesh.</p>
		<h5 class="act-subtitle">Captives and Chains</h5>
		<p class="body">If characters (or NPCs) are imprisoned:</p>
		<ul class="body-list">
			<li>Rusted chains fixed into the cave wall</li>
		</ul>
		<p class="body"><strong>Escape Options</strong></p>
		<ul class="body-list">
			<li>Strength DC 14: Tear chain from wall</li>
			<li>Strength DC 18: Break manacles</li>
			<li>Sleight of Hand DC 12: Unlock chains</li>
			<li>Assisted Strength: Reduce DC</li>
		</ul>
		<p class="body">
			Optional atmosphere: a decomposed body still chained for the barghest’s
			amusement.
		</p>
		<div class="dm-block">
			<p class="dm-title">Stealth and Distraction</p>
			<p class="body">
				Distracting Mogawg outside the cave significantly lowers stealth
				difficulty and allows rescue or ambush.
			</p>
		</div>
		<h5 class="act-subtitle">The Final Confrontation</h5>
		<p class="body">Eventually, confrontation is unavoidable.</p>
		<p class="body">Possible configurations:</p>
		<ul class="body-list">
			<li>Barghest alone</li>
			<li>Barghest with surviving orcs</li>
			<li>A wounded, resource-drained party</li>
		</ul>
		<p class="body">
			A party of 4–5 level 4 characters can handle the fight, but poor choices
			may result in deaths. This is acceptable for a one-shot.
		</p>
		<h5 class="act-subtitle">Alternative Ending: No Final Fight</h5>
		<p class="body">
			If the party locates the lair but most enemies in town remain alive, the
			GM may resolve the finale narratively.
		</p>
		<ul class="body-list">
			<li>The party returns with proof</li>
			<li>Naa Mio attempts to flee</li>
			<li>The GM may call for a roll to see if he escapes</li>
			<li>The real mayor’s body is eventually found</li>
		</ul>
		<p class="body">The illusion collapses. The town begins to recover.</p>
		<div class="dm-block">
			<p class="dm-title">Ending Without the Final Battle</p>
			<p class="body">Exposure can be as powerful as violence.</p>
			<p class="body">
				Use this ending if time or pacing demands it.
			</p>
		</div>
		<h5 class="act-subtitle">Resolution</h5>
		<p class="body">With Mogawg defeated or exposed:</p>
		<ul class="body-list">
			<li>Fear loses its anchor</li>
			<li>Orcs scatter or surrender</li>
			<li>Littlefare survives, though scarred</li>
		</ul>
		<p class="body">
			Political cleanup can be summarized quickly. The important thing is
			closure.
		</p>
		<h5 class="act-subtitle">End of Act III</h5>
		<p class="body">The truth is revealed.</p>
		<p class="body">The lie is broken.</p>
		<p class="body">The town is free.</p>

